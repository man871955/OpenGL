void renderModel(Shader& shader,Model& mod,glm::vec3 pos,float scale,float x = 0,float y = 0,float z = 0) {
	glm::mat4 model(1.0f);
	model = glm::mat4(1.0f);
	model = glm::translate(model, pos);
	model = glm::rotate(model, glm::radians(-x * 90.0f), glm::vec3(1.0f, 0.0f, 0.0f));
	model = glm::rotate(model, glm::radians(-y * 90.0f), glm::vec3(0.0f, 1.0f, 0.0f));
	model = glm::rotate(model, glm::radians(-z * 90.0f), glm::vec3(0.0f, 0.0f, 1.0f));
	model = glm::scale(model, glm::vec3(scale));
	shader.setMat4("model", model);
	shader.setMat3("normalMatrix", glm::transpose(glm::inverse(glm::mat3(model))));
	mod.Draw(shader);
	glBindVertexArray(0);
}
